public class TypeDefaultValue{
	static char a;
	static double b;
	static long c;
	static boolean d;
	static float e;
	static short f;
	static int j;
	static String h;
	static int m[];
	static String k[];
	
	
public static void main(String[] args) {
System.out.println("char"+"\t"+a);
System.out.println("double"+"\t"+b);
System.out.println("long"+"\t"+c);
System.out.println("double"+"\t"+d);
System.out.println("float"+"\t"+e);
System.out.println("short"+"\t"+f);
System.out.println("int"+"\t"+j);
System.out.println("String"+"\t"+h);
System.out.println("int[]"+"\t"+m);
System.out.println("String[]"+"\t"+k);

}
}
	
	
	
	
	
	
	